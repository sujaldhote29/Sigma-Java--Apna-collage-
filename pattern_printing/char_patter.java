public class char_patter {
    public static void main(String args[]) {
        int n = 4;
        char ch = 'A';
        for (int lines = 1; lines <= n; lines++) {
            for (char c = 1; c <= lines; c++) {
                System.out.print(ch);
                ch++;
            }
            System.out.println(); 


            // dddd
        }
    }
}
